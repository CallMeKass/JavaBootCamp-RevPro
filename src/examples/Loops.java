package examples;

public class Loops {
	public static void main(String[] args) {
		//For Loop
		for(int i=1; i<=3; i++) {
			System.out.println("For loop iteration count: " + i);
		}
		
		//While Loop
		int j = 1;
		while(j<=3) {
			System.out.println("While loop iteration count: " + j);
			j++;
		}
	}
}
